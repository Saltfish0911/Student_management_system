package system_pac;

public class Student {
	/*
	 * 学生信息有：
	 * 学号
	 * 名字
	 * 性别
	 * 年龄
	 * 绩点
	 */
	private int id;
	private String name;
	private String sex;
	private int age;
	private int score;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
	
	
	
}
